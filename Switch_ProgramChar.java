class Switch_ProgramChar 
{
	public static void main(String[] args) 
	{
		char c='a';
		switch(c)
		{
			case 'a':System.out.println("case 1");
			    break;
			case 'b':System.out.println("case 2");
			    break;
			case 'c':System.out.println("case 3");
			    break;
			default:System.out.println("Wrong choice");
		}
	}
}