class Switch_Program 
{
	public static void main(String[] args) 
	{
		int c=1;
		switch(c)
		{
			case 1:System.out.println("case 1");
			    break;
			case 2:System.out.println("case 2");
			    break;
			case 3:System.out.println("case 3");
			    break;
			default:System.out.println("Wrong choice");
		}
	}
}
